export const translate = (key, language) => {
  return translations[language]?.[key] || key;
};

// Helper function to convert ISO date or date string to relative time
export const getRelativeTime = (date) => {
  const now = new Date();
  const then = new Date(date);
  const diffInSeconds = Math.floor((now - then) / 1000);
  
  if (diffInSeconds < 60) return 'Just now';
  
  const diffInMinutes = Math.floor(diffInSeconds / 60);
  if (diffInMinutes < 60) {
    return diffInMinutes === 1 ? '1 hour ago' : `${diffInMinutes} hours ago`;
  }
  
  const diffInHours = Math.floor(diffInMinutes / 60);
  if (diffInHours < 24) {
    return diffInHours === 1 ? '1 hour ago' : `${diffInHours} hours ago`;
  }
  
  const diffInDays = Math.floor(diffInHours / 24);
  if (diffInDays < 7) {
    return diffInDays === 1 ? '1 day ago' : `${diffInDays} days ago`;
  }
  
  const diffInWeeks = Math.floor(diffInDays / 7);
  if (diffInWeeks < 4) {
    return diffInWeeks === 1 ? '1 week ago' : `${diffInWeeks} weeks ago`;
  }
  
  const diffInMonths = Math.floor(diffInDays / 30);
  if (diffInMonths < 12) {
    return diffInMonths === 1 ? '1 month ago' : `${diffInMonths} months ago`;
  }
  
  const diffInYears = Math.floor(diffInDays / 365);
  return diffInYears === 1 ? '1 year ago' : `${diffInYears} years ago`;
};

// Helper function to translate relative dates like \"2 days ago\", \"1 week ago\", \"Just now\"
export const translateRelativeDate = (dateString, language) => {
  if (!dateString) return dateString;
  
  // If it's an ISO date or date object, convert it to relative time first
  if (dateString.includes('-') || dateString instanceof Date) {
    dateString = getRelativeTime(dateString);
  }
  
  // Handle \"Just now\"
  if (dateString === 'Just now') {
    return translate('Just now', language);
  }
  
  // Match patterns like \"1 week ago\", \"2 days ago\", \"3 hours ago\"
  const match = dateString.match(/^(\d+)\s+(hour|hours|day|days|week|weeks|month|months|year|years)\s+ago$/);
  
  if (match) {
    const number = match[1];
    const unit = match[2];
    
    // Translate the unit (now just the word without prepositions)
    const translatedUnit = translate(unit + ' ago', language);
    
    // Format based on language
    if (language === 'Spanish') {
      // Spanish: \"hace 2 días\", \"hace 1 día\"
      return `hace ${number} ${translatedUnit}`;
    } else if (language === 'French') {
      // French: \"il y a 2 jours\", \"il y a 1 jour\"
      return `il y a ${number} ${translatedUnit}`;
    } else if (language === 'German') {
      // German: \"vor 2 Tagen\", \"vor 1 Tag\"
      return `vor ${number} ${translatedUnit}`;
    } else {
      // English: \"2 days ago\"
      return `${number} ${translatedUnit} ago`;
    }
  }
  
  // Return original if no pattern matches
  return dateString;
};
